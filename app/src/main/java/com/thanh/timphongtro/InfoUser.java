package com.thanh.timphongtro;

public class InfoUser {
    public String hinh;
    public String ten;
    public String ngaySinh;
    public String sdt;
    public String diaChi;

    public InfoUser() {
    }

    public InfoUser(String hinh, String ten, String ngaySinh, String sdt, String diaChi) {
        this.hinh = hinh;
        this.ten = ten;
        this.ngaySinh = ngaySinh;
        this.sdt = sdt;
        this.diaChi = diaChi;
    }
}
